package ch06.exercise.exam17;

public class Printer {
	
	// 메서드 오버로딩
	static void println(String param) {
		System.out.println(param);
	}
	static void println(int param) {
		System.out.println(param);
	}
	static void println(boolean param) {
		System.out.println(param);
	}
	static void println(double param) {
		System.out.println(param);
	}

}
